# difference between Linked list and array
# There are 4 basic operation in a collection
'''
1. Read
2. Write
3. Update
4. Delete
'''

# Array
# 1. Read :  fast, only need location or index of item to access
# 2. Write/Insert: slow, need to displace elements
# 3. Update : slow, need to displace elements
# 4. Delete: Slow, need to displace elements


# Linked List
# 1. Read :  slow, need to iterate one by one on each element
# 2. Write/Insert: slow in singly linked list, need to iterate one by one on each element
# 3. Update : fast, no need to displace elements just change links
# 4. Delete: fast, no need to displace elements just change links