# List
# list is a collection of objects which are allocated contigous memory. List items are arranged as per Index. They are ordered.
# To access a list item we can use there respected index.

# define a list in python
# 1. use of square brackets
grocery_list = ["floor", "sugar"]
print(grocery_list)

# 2. use list() constructor, this method works for creating list from another already created collection
shoping_list = list(("Shirts", "shoes"))
print(shoping_list)

# access a list item via index
print(shoping_list[0])

# getting list length
print(len(shoping_list))

